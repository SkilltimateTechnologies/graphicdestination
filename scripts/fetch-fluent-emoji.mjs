#!/usr/bin/env node
/*
 * Fetch Microsoft Fluent Emoji (MIT) — the "Smileys & Emotion" group, 3D style —
 * into the client as static assets + a generated manifest module.
 *
 *   node scripts/fetch-fluent-emoji.mjs
 *
 * Output:
 *   client/public/emoji/fluent/3d/<slug>.png   — the 3D renders (served as /emoji/...)
 *   client/src/engine/emojiData.js             — generated manifest (FLUENT_EMOJI[])
 *
 * Idempotent: existing PNGs are skipped, so re-runs only fetch what's missing.
 * Assets come from raw.githubusercontent.com (no API rate limit); only the
 * folder listing uses the GitHub API (2 calls).
 */
import { mkdir, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const OUT_DIR = path.join(ROOT, "client", "public", "emoji", "fluent", "3d");
const MANIFEST = path.join(ROOT, "client", "src", "engine", "emojiData.js");

const REPO = "microsoft/fluentui-emoji";
const API = `https://api.github.com/repos/${REPO}`;
const RAW = `https://raw.githubusercontent.com/${REPO}/main/assets`;
const GROUP = "Smileys & Emotion";
const UA = { headers: { "User-Agent": "gd-fluent-fetch" } };

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function fetchRetry(url, opts, tries = 3) {
  for (let i = 0; i < tries; i++) {
    try {
      const r = await fetch(url, opts);
      if (r.ok) return r;
      if (r.status === 404) return r; // definitive; caller handles
      if (i < tries - 1) await sleep(300 * (i + 1));
    } catch {
      if (i < tries - 1) await sleep(300 * (i + 1));
    }
  }
  return null;
}

async function jget(url) {
  const r = await fetchRetry(url, UA);
  if (!r || !r.ok) throw new Error(`GET failed: ${url}`);
  return r.json();
}

/** Bounded-concurrency map. */
async function pool(items, n, fn) {
  const out = new Array(items.length);
  let i = 0;
  await Promise.all(
    Array.from({ length: n }, async () => {
      while (i < items.length) {
        const idx = i++;
        try {
          out[idx] = await fn(items[idx], idx);
        } catch (e) {
          out[idx] = { __err: String(e?.message || e) };
        }
      }
    })
  );
  return out;
}

const slug = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
const fileBase = (s) => s.toLowerCase().replace(/ /g, "_"); // Fluent file naming keeps hyphens

async function main() {
  console.log("Listing emoji folders…");
  const root = await jget(`${API}/git/trees/main`);
  const assetsNode = root.tree.find((t) => t.path === "assets");
  const assetsTree = await jget(`${API}/git/trees/${assetsNode.sha}`);
  const folders = assetsTree.tree.filter((t) => t.type === "tree").map((t) => t.path);
  console.log(`  ${folders.length} folders`);

  console.log(`Fetching metadata + filtering group="${GROUP}"…`);
  const metas = await pool(folders, 24, async (folder) => {
    const m = await jget(`${RAW}/${encodeURIComponent(folder)}/metadata.json`);
    return { folder, group: m.group, cldr: m.cldr, glyph: m.glyph, unicode: m.unicode, keywords: m.keywords };
  });
  const se = metas.filter((m) => m && !m.__err && m.group === GROUP);
  const metaErrs = metas.filter((m) => m && m.__err).length;
  console.log(`  ${se.length} in group (${metaErrs} metadata errors)`);

  console.log("Downloading 3D PNGs…");
  await mkdir(OUT_DIR, { recursive: true });
  const results = await pool(se, 16, async (m) => {
    const id = slug(m.folder);
    const dest = path.join(OUT_DIR, `${id}.png`);
    if (existsSync(dest)) return { id, m, skipped: true };
    const candidates = [
      `${fileBase(m.folder)}_3d.png`,
      `${fileBase(m.folder).replace(/[’']/g, "")}_3d.png`,
      `${fileBase(m.folder).replace(/-/g, "_")}_3d.png`,
    ];
    for (const c of candidates) {
      const r = await fetchRetry(`${RAW}/${encodeURIComponent(m.folder)}/3D/${c}`, UA);
      if (r && r.ok) {
        const buf = Buffer.from(await r.arrayBuffer());
        await writeFile(dest, buf);
        return { id, m, bytes: buf.length };
      }
    }
    return { id, m, missing: true };
  });

  const good = results.filter((r) => r && !r.missing && !r.__err);
  const missing = results.filter((r) => r && r.missing);
  const seen = new Set();
  const manifest = [];
  for (const r of good) {
    if (seen.has(r.id)) continue;
    seen.add(r.id);
    manifest.push({
      id: r.id,
      name: r.m.cldr,
      glyph: r.m.glyph,
      unicode: r.m.unicode,
      keywords: (r.m.keywords || []).slice(0, 8),
      file: `/emoji/fluent/3d/${r.id}.png`,
    });
  }
  manifest.sort((a, b) => a.name.localeCompare(b.name));

  const header =
    `/* AUTO-GENERATED by scripts/fetch-fluent-emoji.mjs — do not edit by hand.\n` +
    `   Source: Microsoft Fluent Emoji (MIT). Group: ${GROUP}. Style: 3D.\n` +
    `   ${manifest.length} emoji. Regenerate: node scripts/fetch-fluent-emoji.mjs */\n`;
  await writeFile(MANIFEST, header + `export const FLUENT_EMOJI = ${JSON.stringify(manifest)};\n`);

  console.log(`\nDone: ${manifest.length} emoji written to the manifest.`);
  if (missing.length) console.log(`Missing (${missing.length}): ${missing.map((m) => m.id).join(", ")}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
